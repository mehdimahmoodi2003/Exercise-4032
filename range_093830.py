#دریافت عدد از کاربر 
x=float(input("عدد را وارد کنید:"))

#تبدیل به یک مقدار کیفی
if x>=100:    #اگر بزرگتر از 100 بود مقداری به آن تعلق نمی گیرد 
    print("عدد وارد شده درست نیست")
elif x>90:
    print("عالی")
elif x>70:
    print("خوب")
elif x>50:
    print("متوسط")
elif x<50:
    print("مردود")